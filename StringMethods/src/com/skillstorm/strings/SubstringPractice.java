package com.skillstorm.strings;

public class SubstringPractice {
	
	public static void main(String[] args) {
		String hello = "Hello world!";
		System.out.println(hello.substring(1, 5));
		System.out.println(hello.substring(6, 11));
		System.out.println(hello.substring(4,5));
		System.out.println(hello.substring(hello.length() -1));
	}

}
